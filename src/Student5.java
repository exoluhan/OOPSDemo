
public class Student5 {
	
	int rollNo;
    String name;
    int age;
	public Student5(int rollNo, String name, int age) {
		super();
		this.rollNo = rollNo;
		this.name = name;
		this.age = age;
	}
	@Override
	public String toString() {
		return "Student5 [rollNo=" + rollNo + ", name=" + name + ", age=" + age + "]";
	}
    
    
    

}
